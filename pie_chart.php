			function setChart() {

                    var chart = $('#container').highcharts();
                    var title_text = $('#combo option:selected').text();
                    
                    var list_text = $('#main_list option:selected').text();
                                                                                
                    while (chart.series.length > 0)
                                chart.series[0].remove(true);
                     
                    chart.addSeries({id: "default",
                            name: "Company",
                            data: [],
							type: "pie"
                    });        
                            
                    chart.yAxis[0].setTitle({text: list_text});
                    chart.xAxis[0].setTitle({text: title_text});
                    chart.tooltip.options.formatter = function(){return  '<b>' + this.point.name +'</b><br><br/>'+title_text+':' + this.point.y +'<br/>'};
                    
                
            }; // setChart

			function addCompany() {
			
